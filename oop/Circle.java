package in.com.rays.oop;

public class Circle extends Shape {
	

	private double radius;
    double PI=3.14;
	public Circle(int i) {
		 radius=i;
	}

	public double getRadius() {
		return radius;
	}

	public double area () {
		
		return  PI*radius*radius;
		
	
	
	
	}
	

}
	
	
      
	

	
	

	
	
	


